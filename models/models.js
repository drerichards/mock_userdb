const mongoose = require('mongoose')
mongoose.Promise = global.Promise

const UserSchema = mongoose.Schema({
    first_name: {
        type: String,
        required: true
    },
    last_name: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    }
})

const User = mongoose.model('User', UserSchema)

module.exports = { User }